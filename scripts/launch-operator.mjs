/**
 * Launch Operator — automated Preview readiness gate (G.10-Q)
 *
 * Run: npm run launch:operator
 * Optional: PREVIEW_URL, SKIP_PREVIEW_FIX=1, SKIP_PLAYWRIGHT=1
 */
import { randomBytes } from "crypto";
import { config } from "dotenv";
import { existsSync, readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve } from "path";
import { spawnSync } from "child_process";
import { createClient } from "@supabase/supabase-js";
import {
  STAGING_REF,
  PROD_REF,
  maskRef,
  supabaseJwtRef,
} from "./lib/supabase-env-audit.mjs";
import {
  buildPreviewEnvMap,
  syncPreviewEnvFromMap,
} from "./lib/sync-vercel-preview-env.mjs";
import { scanPreviewBundle, cleanVercelEnv } from "./lib/scan-preview-bundle.mjs";
import { resolveLatestPreviewUrl } from "./lib/resolve-latest-preview.mjs";
import { probePreviewProviderGuard } from "./lib/preview-provider-guard.mjs";
import { resolveVercelProtectionBypass } from "./lib/resolve-vercel-protection-bypass.mjs";

const EMAIL = "visualqa@influexai.test";
const REPORT_PATH = resolve(process.cwd(), "docs/reports/launch-operator-g10q.md");
const ENV_LOCAL = resolve(process.cwd(), ".env.local");

config({ path: ENV_LOCAL });
delete process.env.VERCEL_DEBUG;
delete process.env.DEBUG;

let sessionPassword = process.env.VISUAL_QA_PASSWORD?.trim() ?? "";
let passwordAutoGenerated = false;

if (!sessionPassword) {
  sessionPassword = randomBytes(24).toString("base64url");
  process.env.VISUAL_QA_PASSWORD = sessionPassword;
  passwordAutoGenerated = true;
}

function fail(message, code = 1) {
  console.error(`❌ ${message}`);
  process.exit(code);
}

function auditLocalSafety() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL ?? "";
  const anonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ?? "";
  const stripeSecret = process.env.STRIPE_SECRET_KEY?.trim() ?? "";
  const stripePub = process.env.NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY?.trim() ?? "";
  const providersDisabled = (process.env.PROVIDERS_DISABLED ?? "").trim().toLowerCase();

  const ref = maskRef(url);
  const anonJwt = supabaseJwtRef(anonKey);

  return {
    env_local_present: existsSync(ENV_LOCAL),
    providers_disabled: ["true", "1", "yes"].includes(providersDisabled),
    allow_safe_dev_provider_smoke:
      process.env.ALLOW_SAFE_DEV_PROVIDER_SMOKE?.trim().toLowerCase() === "true",
    stripe_mode: process.env.STRIPE_MODE ?? "(unset)",
    stripe_live:
      stripeSecret.startsWith("sk_live_") || stripePub.startsWith("pk_live_"),
    local_supabase_ref: ref,
    local_anon_jwt_ref: anonJwt,
    url_anon_match: ref === anonJwt && ref === STAGING_REF,
  };
}

function deployPreview() {
  const result = spawnSync("npx", ["vercel", "--yes"], {
    cwd: process.cwd(),
    encoding: "utf8",
    env: cleanVercelEnv(process.env),
    stdio: ["pipe", "pipe", "pipe"],
    shell: process.platform === "win32",
  });
  const out = `${result.stdout ?? ""}\n${result.stderr ?? ""}`;
  const urlMatch = out.match(
    /https:\/\/influexai-[a-z0-9]+-paschalidisgeorgios-projects\.vercel\.app/
  );
  return { ok: result.status === 0, deployment_url: urlMatch?.[0] ?? null };
}

function sleepMs(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function waitForPreviewUrl(preferred) {
  for (let i = 0; i < 12; i += 1) {
    const url = resolveLatestPreviewUrl(preferred) ?? preferred;
    if (url) return url.replace(/\/$/, "");
    await sleepMs(10000);
  }
  return (preferred ?? "").replace(/\/$/, "");
}

function runEnsureUser() {
  return spawnSync("node", ["scripts/ensure-staging-visual-qa-user.mjs"], {
    cwd: process.cwd(),
    encoding: "utf8",
    env: { ...process.env, VISUAL_QA_PASSWORD: sessionPassword },
    stdio: ["pipe", "pipe", "pipe"],
  });
}

function runPlaywright(previewUrl) {
  const bypass = resolveVercelProtectionBypass(process.env);
  return spawnSync(
    "npx",
    [
      "playwright",
      "test",
      "tests/e2e/flows/visual-qa-auth-truth.test.ts",
      "--config=playwright.visual-qa-auth.config.ts",
    ],
    {
      cwd: process.cwd(),
      encoding: "utf8",
      env: {
        ...cleanVercelEnv(process.env),
        VISUAL_QA_PASSWORD: sessionPassword,
        PREVIEW_URL: previewUrl,
        PLAYWRIGHT_BASE_URL: previewUrl,
        ...(bypass ? { VERCEL_AUTOMATION_BYPASS_SECRET: bypass } : {}),
      },
      stdio: ["pipe", "pipe", "pipe"],
      shell: process.platform === "win32",
    }
  );
}

function writeReport(report) {
  mkdirSync(resolve(process.cwd(), "docs/reports"), { recursive: true });
  const ui = report.playwright?.ui;
  const bundle = report.preview_bundle ?? {};
  const md = `# Launch Operator — G.10-Q

Generated: ${new Date().toISOString()}

## Launch Gate

| Gate | Result |
|------|--------|
| Direct Supabase Auth | ${report.direct_supabase_sign_in_ok ? "PASS" : "FAIL"} |
| Preview Bundle (staging refs) | ${bundle.bundle_gate_pass ? "PASS" : "FAIL"} |
| UI Login | ${ui?.ui_login_ok ? "PASS" : report.playwright?.skipped ? "SKIPPED" : "FAIL"} |
| Provider Guard | ${report.provider_guard?.pass ? "PASS" : "FAIL"} |

## Direct Supabase Auth

- User: \`${EMAIL}\`
- Result: ${report.direct_supabase_sign_in_ok ? "PASS" : "FAIL"}
- user_id: ${report.direct_supabase_user_id ?? "n/a"}
- error: ${report.direct_supabase_error_code ?? "none"} / ${report.direct_supabase_error_message ?? "none"}
- password logged: **false**
- password auto-generated this run: ${report.password_auto_generated ? "yes (synced to staging)" : "no"}

## Preview Bundle

- URL: ${report.preview_url ?? "n/a"}
- Supabase URL ref(s): ${bundle.url_refs?.join(", ") ?? "n/a"}
- Anon JWT ref(s): ${bundle.anon_jwt_refs?.join(", ") ?? "n/a"}
- Expected ref: \`${STAGING_REF}\`
- Production ref in bundle: ${bundle.production_url_ref || bundle.production_anon_ref ? "YES" : "no"}
- \`url_anon_mismatch\`: ${bundle.url_anon_mismatch ? "true" : "false"}
- Preview env fix attempted: ${report.preview_env_fix_attempted ? "yes" : "no"}

## UI Login & Dashboard

- UI Login: ${ui?.ui_login_ok ? "PASS" : "FAIL/skipped"}
- Final URL: ${ui?.final_url ?? "n/a"}
- Error text: ${ui?.error_text ?? "none"}
- Session set: ${ui?.session_cookie_set || ui?.local_storage_session ? "yes" : "no"}
- Dashboard: ${ui?.dashboard_reachable ? "yes" : "no"}
- Image Generator: ${ui?.image_generator_reachable ? "yes" : "no"}
- Credits visible: ${ui?.credits_visible ?? "n/a"}
- Provider-disabled banner: ${ui?.provider_disabled_banner ? "yes" : "no"}
- Gallery: ${ui?.gallery_reachable ? "yes" : "no"}

## Provider Guard

- Status: ${report.provider_guard?.pass ? "PASS" : "FAIL"}
- code: ${report.provider_guard?.code ?? "n/a"}
- generationId: ${report.provider_guard?.has_generation_id ? "yes" : "no"}
- imageUrl: ${report.provider_guard?.has_image_url ? "yes" : "no"}

## Blockers

${report.blockers.length ? report.blockers.map((b) => `- ${b}`).join("\n") : "- none"}

## Diagnosis

**${report.diagnosis ?? "pending"}**

---
No secrets in this report.
`;
  writeFileSync(REPORT_PATH, md);
  console.log(`\nReport: ${REPORT_PATH}`);
}

function stopOnBundleMismatch(report, bundle, previewUrl) {
  report.diagnosis = "preview_bundle_env_mismatch";
  report.blockers.push(
    `preview_bundle_mismatch: url_refs=[${bundle.url_refs?.join(",") ?? ""}] anon_jwt_refs=[${bundle.anon_jwt_refs?.join(",") ?? ""}] expected=${STAGING_REF}`
  );
  report.preview_url = previewUrl;
  report.preview_bundle = bundle;
  writeReport(report);
  console.error("\n❌ Preview bundle env mismatch — stopped.");
  console.error(`   URL refs: ${bundle.url_refs?.join(", ") || "(none)"}`);
  console.error(`   Anon JWT refs: ${bundle.anon_jwt_refs?.join(", ") || "(none)"}`);
  console.error(`   Expected both: ${STAGING_REF}`);
  process.exit(3);
}

console.log("=== Launch Operator (G.10-Q) ===\n");

if (passwordAutoGenerated) {
  console.log("ℹ️  VISUAL_QA_PASSWORD auto-generated for this run (not logged, synced to staging)\n");
}

const safety = auditLocalSafety();
if (!safety.env_local_present) fail(".env.local missing");
if (!safety.providers_disabled) fail("PROVIDERS_DISABLED must be true");
if (safety.allow_safe_dev_provider_smoke) fail("ALLOW_SAFE_DEV_PROVIDER_SMOKE must be false");
if (safety.stripe_mode !== "test") fail("STRIPE_MODE must be test");
if (safety.local_supabase_ref !== STAGING_REF) fail(`Local Supabase must be staging (${STAGING_REF})`);
if (!safety.url_anon_match) fail("Local Supabase URL and anon key must match staging ref");
if (safety.stripe_live) fail("Stripe live keys blocked");

const url = process.env.NEXT_PUBLIC_SUPABASE_URL ?? "";
const anonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY ?? "";

const report = {
  phase: "launch-operator-g10q",
  safety,
  password_auto_generated: passwordAutoGenerated,
  preview_env_fix_attempted: false,
  preview_url: null,
  preview_bundle: null,
  ensure_user: null,
  direct_supabase_sign_in_ok: false,
  direct_supabase_user_id: null,
  direct_supabase_error_code: null,
  direct_supabase_error_message: null,
  provider_guard: null,
  playwright: null,
  diagnosis: null,
  blockers: [],
  secrets_logged: false,
};

console.log("Step 1: Ensure visualqa on staging…");
const ensure = runEnsureUser();
report.ensure_user = { ok: ensure.status === 0, exit_code: ensure.status ?? 1 };
if (!report.ensure_user.ok) {
  console.error(ensure.stderr || ensure.stdout);
  report.blockers.push("ensure_visualqa_failed");
  writeReport(report);
  fail("ensure-staging-visual-qa-user failed", 2);
}
console.log("✅ visualqa ensured (plan=starter, credits=75)\n");

console.log("Step 2: Direct Supabase signInWithPassword…");
const anon = createClient(url, anonKey, { auth: { persistSession: false } });
const { data, error } = await anon.auth.signInWithPassword({
  email: EMAIL,
  password: sessionPassword,
});

report.direct_supabase_sign_in_ok = Boolean(data.user && !error);
report.direct_supabase_user_id = data.user?.id ?? null;
report.direct_supabase_error_code = error?.code ?? null;
report.direct_supabase_error_message = error?.message ?? null;

console.log(
  report.direct_supabase_sign_in_ok
    ? `✅ Direct sign-in PASS (${report.direct_supabase_user_id})`
    : `❌ Direct sign-in FAIL (${report.direct_supabase_error_code})`
);

if (!report.direct_supabase_sign_in_ok) {
  report.diagnosis = "direct_supabase_sign_in_failed";
  report.blockers.push("direct_supabase_sign_in_failed");
  writeReport(report);
  fail("Direct Supabase sign-in failed", 2);
}

let previewUrlFromDeploy = null;

if (process.env.SKIP_PREVIEW_FIX !== "1") {
  console.log("\nStep 3: Sync Preview env from .env.local + deploy (no --prod)…");
  report.preview_env_fix_attempted = true;
  const sync = syncPreviewEnvFromMap(buildPreviewEnvMap(process.env), process.env);
  console.log(
    sync.ok
      ? `✅ Preview env synced (${sync.synced.length} keys, production untouched)`
      : `⚠️  Preview env sync partial (${sync.failed.length} failed)`
  );
  const deploy = deployPreview();
  console.log(deploy.ok ? "✅ Preview deploy triggered" : "⚠️  Preview deploy may have failed");
  if (deploy.deployment_url) {
    previewUrlFromDeploy = deploy.deployment_url;
  }
} else {
  console.log("\nStep 3: ⏭️  SKIP_PREVIEW_FIX=1");
}

console.log("\nStep 4: Resolve Preview URL + bundle scan…");
let previewUrl =
  process.env.PREVIEW_URL?.replace(/\/$/, "") ??
  previewUrlFromDeploy ??
  resolveLatestPreviewUrl(null) ??
  (await waitForPreviewUrl(null));

if (!previewUrl) fail("Could not resolve Preview URL");

report.preview_url = previewUrl;
console.log(`Preview URL: ${previewUrl}`);

let bundle = await scanPreviewBundle(previewUrl);
report.preview_bundle = bundle;

console.log(`   URL refs: ${bundle.url_refs?.join(", ") || "(none)"}`);
console.log(`   Anon JWT refs: ${bundle.anon_jwt_refs?.join(", ") || "(none)"}`);

if (!bundle.bundle_gate_pass) {
  stopOnBundleMismatch(report, bundle, previewUrl);
}

console.log("✅ Preview bundle PASS (staging URL + anon JWT)\n");

if (process.env.SKIP_PLAYWRIGHT !== "1") {
  console.log("Step 5: Playwright UI login + dashboard read-only…");
  const pw = runPlaywright(previewUrl);
  report.playwright = { exit_code: pw.status ?? 1 };
  try {
    report.playwright.ui = JSON.parse(
      readFileSync(resolve(process.cwd(), "scripts/visual-qa-auth-truth-ui.json"), "utf8")
    );
  } catch {
    report.playwright.ui = null;
  }
  if ((pw.status ?? 1) !== 0 || !report.playwright.ui?.ui_login_ok) {
    report.diagnosis = "ui_login_failed";
    report.blockers.push("ui_login_failed");
    console.warn("⚠️  UI login probe FAIL");
  } else if (!report.playwright.ui?.dashboard_reachable) {
    report.diagnosis = "dashboard_unreachable";
    report.blockers.push("dashboard_unreachable");
    console.warn("⚠️  Dashboard read-only probe FAIL");
  } else {
    console.log("✅ UI login + dashboard probes PASS");
  }
} else {
  report.playwright = { skipped: true, reason: "SKIP_PLAYWRIGHT" };
}

console.log("\nStep 6: Provider guard probe…");
report.provider_guard = probePreviewProviderGuard(previewUrl);
console.log(
  report.provider_guard.pass
    ? "✅ Provider guard PASS (PROVIDERS_DISABLED)"
    : `❌ Provider guard FAIL (${report.provider_guard.code ?? report.provider_guard.error ?? "unknown"})`
);

if (!report.provider_guard.pass) {
  report.blockers.push("provider_guard_failed");
}

if (
  report.direct_supabase_sign_in_ok &&
  bundle.bundle_gate_pass &&
  report.playwright?.ui?.ui_login_ok &&
  report.playwright?.ui?.dashboard_reachable &&
  report.provider_guard?.pass
) {
  report.diagnosis = "launch_gates_pass";
}

writeReport(report);

const exitOk =
  report.direct_supabase_sign_in_ok &&
  bundle.bundle_gate_pass &&
  (report.playwright?.exit_code ?? 1) === 0 &&
  Boolean(report.playwright?.ui?.ui_login_ok) &&
  Boolean(report.playwright?.ui?.dashboard_reachable) &&
  Boolean(report.provider_guard?.pass);

console.log("\n=== Launch Operator Result ===");
console.log(
  JSON.stringify(
    {
      direct_supabase_sign_in_ok: report.direct_supabase_sign_in_ok,
      bundle_gate_pass: bundle.bundle_gate_pass,
      ui_login_ok: report.playwright?.ui?.ui_login_ok ?? false,
      dashboard_reachable: report.playwright?.ui?.dashboard_reachable ?? false,
      provider_guard_pass: report.provider_guard?.pass,
      blockers: report.blockers,
      secrets_logged: false,
    },
    null,
    2
  )
);

process.exit(exitOk ? 0 : 1);
